#!/usr/bin/env node
/**
 * Extract verifiable factual claims from report markdown for human review
 * with GPTZero Hallucination Detector (https://gptzero.me/hallucination-detector).
 *
 * Usage:
 *   npm run verify:claims
 *   node scripts/prepare-claim-verification.mjs reports/2026-05-member-state-update.md
 *
 * Output: reports/verification/YYYY-MM-DD-claims-for-review.md
 */
import { readFileSync, writeFileSync, mkdirSync, readdirSync, statSync } from "node:fs";
import { join, dirname, basename } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const reportsDir = join(root, "reports");
const outDir = join(reportsDir, "verification");

const CLAIM_PATTERNS = [
  /\$\d[\d,.]*(?:\s*(?:billion|million|B|M))?\b/gi,
  /\b\d{1,3}(?:,\d{3})+\+?\b/g,
  /\b(?:UN|UNICC|ICC|ITU|WIPO|UNDP|UNICEF|WHO|FAO|IAEA)\b[^.\n]{0,120}\./gi,
  /\b(?:more than|over|approximately|at least|up to)\s+\d+[^.\n]{0,80}\./gi,
  /\b(?:launched|announced|signed|adopted|published|reported)\b[^.\n]{0,100}\./gi,
];

function extractSentences(text) {
  return text
    .replace(/^#+\s.*$/gm, "")
    .replace(/^\*\*.*\*\*$/gm, "")
    .replace(/^>\s.*$/gm, "")
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
    .replace(/\[\^\d+\]/g, "")
    .split(/(?<=[.!?])\s+/)
    .map((s) => s.trim())
    .filter((s) => s.length > 40 && s.length < 500);
}

function scoreClaim(sentence) {
  let score = 0;
  if (/\$\d|billion|million|\d{1,3}(?:,\d{3})/i.test(sentence)) score += 3;
  if (/\b(?:UN|UNICC|ITU|WIPO|UNDP)\b/.test(sentence)) score += 2;
  if (/\b(?:launched|announced|signed|adopted|published|reported|baseline|consolidat)/i.test(sentence))
    score += 2;
  if (/\b(?:may|might|could|suggest|indicate|appears)\b/i.test(sentence)) score -= 1;
  if (/disclaimer|DRAFT|working document|human review/i.test(sentence)) score -= 5;
  return score;
}

function pickClaims(sentences, limit = 25) {
  const scored = sentences
    .map((text, i) => ({ id: i + 1, text, score: scoreClaim(text) }))
    .filter((c) => c.score >= 2)
    .sort((a, b) => b.score - a.score);
  return scored.slice(0, limit);
}

function resolveInputFiles(argv) {
  if (argv.length) return argv.map((p) => join(root, p));
  const files = readdirSync(reportsDir)
    .filter((f) => f.endsWith(".md") && !f.startsWith("."))
    .map((f) => join(reportsDir, f))
    .filter((p) => statSync(p).isFile())
    .sort((a, b) => statSync(b).mtimeMs - statSync(a).mtimeMs);
  return files.slice(0, 3);
}

function buildReviewDoc(sources) {
  const today = new Date().toISOString().slice(0, 10);
  const sections = [];
  let claimNum = 1;
  const allClaims = [];

  for (const file of sources) {
    const text = readFileSync(file, "utf8");
    const sentences = extractSentences(text);
    const claims = pickClaims(sentences);
    sections.push(`## ${basename(file)}\n`);
    for (const c of claims) {
      const line = `${claimNum}. ${c.text}`;
      allClaims.push(line);
      sections.push(line + "\n");
      claimNum++;
    }
    sections.push("");
  }

  const pasteBlock = allClaims.join("\n\n");

  return `# Claim verification checklist — ${today}

**Purpose:** Human review of high-risk factual claims before external distribution.  
**Tool:** [GPTZero Hallucination Detector](https://gptzero.me/hallucination-detector)  
**Status:** DRAFT — automated extraction; not a substitute for source checking.

---

## Instructions

1. Copy the **Claims block** below (or individual numbered claims).
2. Paste into [GPTZero Hallucination Detector](https://gptzero.me/hallucination-detector).
3. For each flagged claim, verify against:
   - \`data/secondary-sources/*.yaml\` (public evidence)
   - \`data/academic-literature/wp15-core-references.yaml\` (peer-reviewed footnotes)
   - Original source URL or DOI
4. Record outcomes in the PR review comment or Notion briefing row.

## Evidence tiers (use in footnotes)

| Tier | Source | Example |
|------|--------|---------|
| A | Peer-reviewed journal / standard | Armbrust 2010 [^1] |
| B | Official UN / agency public page | UNICC annual report |
| C | Standards body public spec | W3C VC Data Model |
| D | Grey literature / think tank | OECD digital gov report |
| E | Automated scan — verify before use | Social monitor signal |

---

## Extracted claims

${sections.join("\n")}

---

## Claims block (paste into GPTZero)

\`\`\`
${pasteBlock}
\`\`\`

---

*Generated by \`npm run verify:claims\` — see docs/ACADEMIC-RIGOUR.md*
`;
}

function main() {
  const inputs = resolveInputFiles(process.argv.slice(2));
  if (!inputs.length) {
    console.error("No report files found.");
    process.exit(1);
  }
  mkdirSync(outDir, { recursive: true });
  const today = new Date().toISOString().slice(0, 10);
  const outPath = join(outDir, `${today}-claims-for-review.md`);
  writeFileSync(outPath, buildReviewDoc(inputs), "utf8");
  console.log(`Wrote ${outPath} (${inputs.length} source file(s))`);
}

main();
